    <h3>Further guidance for Users</h3>
    <p>It is important to consider the nature of your research collaboration with your matched laboratory. Consider
       questions like: Do you need analysis only? Or specific expertise and therefore intellectual property? Often
       called
       IP for short. If they are providing analysis only you will know exactly what analysis you want and why. This is
       more like a ‘service contract’.</p>
    <p>In these cases you will need to, at the least, acknowledge the laboratory and GeoCoLab funding, see GeoCoLab
       acknowledgement protocol, in the acknowledgement section of any publication. Anything that is not a ‘service
       contract’ will involve IP of the laboratory or that of the individuals that work there. Many laboratories will
       have
       an IP and/or data agreement and you should not be surprised if you are asked to sign one. Irrespective of whether
       this is the case, or not, you should collectively (yourself and the laboratory) consider IP, data ownership and
       use
       at the start of the project to protect your IP and that of the collaborating laboratory and individuals. This
       could
       include rights of data use, paper authorship, acknowledgements etc. It is useful to have a plan to revisit any
       agreements as the project evolves to ensure all parties are happy that any original agreement is still fair and
       covers any changes to project input and circumstances.</p>
    <p>Laboratories, or their host institutions, may also expect you to sign-up to ethical agreements and codes of
       practice, especially if you are visiting a laboratory to conduct the analyses yourself. This is all normal, and
       best practice, it is designed to protect you, your collaborators and others. These agreements will be in addition
       to any ethical statements you agreed to as part of your GeoCoLab match.</p>
    <p>When you are thinking about what analyses you need and level of input, it is might be useful to consider the
       following questions:</p>
    <ul class="list-disc">
      <li>What support and input do you need?</li>
      <li>Will you complete the analyses yourself?</li>
      <li>Will you prepare the samples?</li>
      <li>Would you like input into interpretation of the results?</li>
      <li>Would you like input into writing methods e.g. the use of standards, instrument set-up…?</li>
    </ul>
    <p>Answers to such questions will help you to determine the extent of any collaboration agreement and can help frame
       a
       discussion.</p>
