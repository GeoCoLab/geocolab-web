    <h2>Memorandum of Understanding</h2>
    <p class="text-xl text-center">19th June 2022</p>
    <p>Universities and Institutions engaging with GeoCoLab (hereafter named as the partners) are legal entities fully
       empowered to conduct their own affairs.</p>
    <p>The partners have agreed to enter into this Memorandum of Understanding (MOU) with GeoCoLab, which is not legally
       binding, to record their intention to explore providing access to their laboratory facilities for
       under-represented groups.</p>

    <h3>Purpose</h3>
    <p>GeoCoLab exists to reduce inequities in geoscience research by closing the gap in access to analytical
       facilities.
       It is an online collaborative platform that links underserved researchers who have a particular need for
       analytical services (the users) with laboratory facilities who have agreed to offer a quota of pro bono services
       (the partners). The GeoCoLab app matches user need to a partner. The match is anonymous to protect partners and
       users and to facilitate ethical collaboration agreements.</p>
    <p>This is an equal partnership for the mutual benefit of all partners that engage with GeoCoLab.</p>

    <h3>Activities</h3>
    <p>The partners potential collaboration with GeoCoLab, on a non-exclusive basis, will be in the following ways:</p>

    <ol>
      <li>To provide individuals from traditionally under-represented groups with pro bono access to laboratory
          analytical facilities
      </li>
      <li>To provide pro bono access to high grade laboratory facilities to geoscientists in the global south</li>
    </ol>
    <p>Activities to be delivered by the partners:</p>
    <p>Sample analysis as offered by the individual partners; specifics of analysis type and amount to be agreed between
       the individual partner and user</p>
    <p>Access to laboratory facilities to enable use of specific analytical equipment as offered by the individual
       partners; specifics of access and equipment usage to be agreed between the individual partner and user </p>
    <p>Specifics to be agreed with the partner and user can include:</p>
    <ul>
      <li>Number of samples or instrument time</li>
      <li>Preparation of samples</li>
      <li>Data delivery mode e.g. raw data, or interpreted data</li>
      <li>Schedule of analysis and data delivery, including review points</li>
      <li>Authorship of resulting publications</li>
      <li>Acknowledgement in resulting publications</li>
      <li>IP, data ownership and data sharing</li>
    </ul>
    <p>GeoCoLab exists solely to bring partners and users together and cannot remediate any disagreements between the
       party and the user nor be held responsible for any contraventions of agreements made between the party and
       user.</p>
    <p>Guidance for GeoCoLab partners on how they will be expected to interact with users is set out in the GeoCoLab
       code of conduct.</p>

    <h3>Monitoring</h3>
    <p>All partners commit to ongoing monitoring, with the aim of ensuring accountability and performance against
       milestones.</p>
    <p>GeoCoLab will review activities annually to ensure that they are being delivered as agreed, and that they are
       having the intended impact.</p>
    <p>GeoCoLab are responsible for coordinating and reporting on the impact of the activities.</p>
    <p>GeoCoLab will coordinate the monitoring and report back to governing boards as appropriate.</p>

    <h3>Governance</h3>
    <p>Governance arrangements will be through an independent joint committee. The joint committee will be responsible
       for overseeing the activities that GeoCoLab facilitates and ensuring they are fair and equitable in accordance
       with the ethos of the project.</p>

    <h3>Financial contribution</h3>
    <p>There will be no financial transactions between Geocolab and the partners.</p>

    <p>This Memorandum of Understanding will last for one year from the date shown above unless terminated earlier by
       either party.</p>

    <h3>Disclaimer</h3>
    <p>It should be noted that by agreeing to this document and by participating in GeoCoLab, the partners are not
       committing to legally binding obligations. It is intended that the parties and GeoCoLab remain independent of
       each other and that their collaboration does not constitute the creation of a legal entity, nor authorise the
       entry into a commitment for or on behalf of each other.</p>
